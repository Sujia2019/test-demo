package com.cxl.entry;

public class EquipMsg {
    private int id;
    private int equip_id;
    private String eq_name;
    private int attack;
    private int phy_def;
    private int eq_hp;
    private int eq_mp;
    private int eq_value;
    private int eq_place;
    private String details;
    private String quality;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEquip_id() {
        return equip_id;
    }

    public void setEquip_id(int equip_id) {
        this.equip_id = equip_id;
    }

    public String getEq_name() {
        return eq_name;
    }

    public void setEq_name(String eq_name) {
        this.eq_name = eq_name;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

    public int getPhy_def() {
        return phy_def;
    }

    public void setPhy_def(int phy_def) {
        this.phy_def = phy_def;
    }

    public int getEq_hp() {
        return eq_hp;
    }

    public void setEq_hp(int eq_hp) {
        this.eq_hp = eq_hp;
    }

    public int getEq_mp() {
        return eq_mp;
    }

    public void setEq_mp(int eq_mp) {
        this.eq_mp = eq_mp;
    }

    public int getEq_value() {
        return eq_value;
    }

    public void setEq_value(int eq_value) {
        this.eq_value = eq_value;
    }

    public int getEq_place() {
        return eq_place;
    }

    public void setEq_place(int eq_place) {
        this.eq_place = eq_place;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getQuality() {
        return quality;
    }

    public void setQuality(String quality) {
        this.quality = quality;
    }
}
